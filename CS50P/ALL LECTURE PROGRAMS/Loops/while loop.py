# print meow three times using while loop.
# first we create a variable i and assign value as 3.
# if while is true it will loop infinte times to get out of that loop we use some method like 'break'.
# In this program if while is true it will loop every loop it print moew and we subtract one number if while loop get false it stop the loop.
